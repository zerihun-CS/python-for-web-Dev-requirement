# A sequence is a positionally ordered collection of items. 
# And you can refer to any item in the sequence by using its index number e.g., s[0] and s[1].
# In Python, the sequence index starts at 0, not 1. So the first element is s[0] and the second element is s[1].
# If the sequence s has n items, the last item is s[n-1].



